package javacore.forloop;

public class Program2 {
    public static void main(String[] args) {
        //wap to print number from 10 to 1
        System.out.println("Printing the number from 10 to 1 ");
        int num = 10;
        for(int i =num;i>=1;i--)
        {
            System.out.println(i);
        }
    }
}
