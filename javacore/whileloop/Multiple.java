package javacore.whileloop;

public class Multiple {
    public static void main(String[] args) {
        //wap to print all numbers between 1 to 150 multiple of 11.
        int multipleOf=11;
        int num=1;
        while(num<=10){
            System.out.println(multipleOf+" * "+ num + " = "+(multipleOf*num));
            num++;
        }

    }
}
