public class Program13 {
    public static void main(String[] args) {
        //wap to print all even numbers between 100 to 120.
        System.out.print("all even numbers between 100 to 120. :- " );
        for(int i=100;i<=120;i++)
        {
            if(i%2==0)
            {
                System.out.print(i+" ; ");
            }
        }
    }
}
